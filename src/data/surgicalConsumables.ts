// Helper functions
export function getConsumablesByCategory(categoryId: string): SurgicalConsumable[] {
  return surgicalConsumables.filter(c => c.category === categoryId);
}

export function getConsumableById(id: string): SurgicalConsumable | undefined {
  return surgicalConsumables.find(c => c.id === id);
}

export function searchConsumables(query: string): SurgicalConsumable[] {
  const lowerQuery = query.toLowerCase();
  return surgicalConsumables.filter(c =>
    c.name.toLowerCase().includes(lowerQuery) ||
    c.description.toLowerCase().includes(lowerQuery) ||
    c.commonUses.some(use => use.toLowerCase().includes(lowerQuery))
  );
}

